import { Component } from '@angular/core';
import { Platform } from 'ionic-angular';
import { StatusBar } from '@ionic-native/status-bar';
import { SplashScreen } from '@ionic-native/splash-screen';

import { SigninPage } from '../pages/signin/signin';
import { LocalFactory } from './x/storage.utils';
import { userSessionKey } from './common/constant.common';
import { HomePage } from '../pages/home/home';
@Component({
  templateUrl: 'app.html'
})
export class MyApp {
  rootPage: any = HomePage;

  constructor(platform: Platform, statusBar: StatusBar, splashScreen: SplashScreen) {
    console.log('LocalFactory.getItem(userSessionKey)', LocalFactory.getItem(userSessionKey))
    if (LocalFactory.getItem(userSessionKey).token) {
      this.rootPage = HomePage
    } else {
      this.rootPage = SigninPage
    }
    platform.ready().then(() => {
      // Okay, so the platform is ready and our plugins are available.
      // Here you can do any higher level native things you might need.
      statusBar.styleDefault();
      splashScreen.hide();
    });
  }
}

